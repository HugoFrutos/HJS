$(document).ready(function(){
    
    var table = $('#myTable').DataTable({
        "ajax":{
            "url":"../procesos/tiposGasto/mostrar.php",
            "type":"GET"
            //"crossDomain": "true",
            //"dataType": "json",
            //"dataSrc":""
        },
        "columns":[
            {
                "data":"idTipoGasto"
            },
            {
                "data":"tipoGasto"
            }, 
            {
                    sTitle: "Eliminar",
                    mDataProp: "idTipoGasto",
                    sWidth: '7%',
                    orderable: false,
                    render: function(data) {
                        acciones = `<button id="` + data + `" value="Eliminar"  type="button" class="fa fa-times btn btn-danger accionesTabla" >
                                    
                                </button>`;
                        return acciones
                    }
            }, 
            {
                sTitle: "Editar",
                mDataProp: "idTipoGasto",
                sWidth: '7%',
                orderable: false,
                render: function(data) {
                    acciones = `<button id="` + data + `" value="Traer" class="fa fa-pencil-square-o btn btn-primary accionesTabla" data-toggle="modal" data-target="#exampleModal2" type="button"  >
                                    
                                </button>`;
                    return acciones
                }
            },
        ],
        responsive:true,
                "ordering": false


    });
    
$(document).on('click', '.accionesTabla', function() {
    var id = this.id;
    var val = this.value;

    switch (val) {
        case "Traer":
                    $.ajax({
                        method : "GET",
                        url : "../procesos/tiposGasto/traer.php",
                        data:'idTipoGasto='+id
                    }).done(function(msg) {
                        var dato=JSON.parse(msg);
                        
				        $('#txttipoe').val(dato['tipoGasto']);

                        $('#btneditar').unbind().click(function(){
                            vacios = validarFormVacio('frmgastoe');
                            
                            
                            if(vacios <= 0)
                                {
                            tipo = $("#txttipoe").val();
                            
                             oka = {
						                "txttipoe" : tipo , "idTipoGasto" : id,
                                };
                            //alert(oka);
                            //alert(JSON.stringify(oka));
                            $.ajax({
                                method : "POST",
                                //contentType: 'application/json; charset=utf-8',
                                url : "../procesos/tiposGasto/editar.php",
                                data : oka
                                }).done(function(msg) {
                                alertify.success("Tipo de gasto editado correctamente");
                                table.ajax.reload();
                                });                               
                                    
                                }
                            else{
                                alertify.error("Complete los datos");
                            }

                        });
                    });
            break;
        case "Eliminar":

                alertify.confirm('Tipo de gasto', 'Â¿Esta seguro que desea eliminar este tipo de gasto?', function() {
                    $.ajax({
                        type: "POST",
                        url: "../procesos/tiposGasto/eliminar.php",
                        data: "id=" + id
                    }).done(function(msg) {
                        alertify.success("Tipo de gasto eliminado correctamente");
                        table.ajax.reload();
                    });
                }, function() {

                });
            break;
        default:
            alert("No existe el valor");
            break;
    }
    });    
    
    
    
    $('#btnregistrar').click(function(){
        vacios = validarFormVacio('frmgasto');
        if(vacios <= 0 )
            {
            datos=$('#frmgasto').serialize();
            $.ajax({
               type:'post',
                url:'../procesos/tiposGasto/registrar.php',
                data:datos,
                success:function(r)
                {
                    
                    if(r==1)
                        {
                            alertify.success("Tipo de gasto registrado correcamente");
                            table.ajax.reload();
                        }
                    else if(r==0)
                        {
                            alertify.error("No se pudo registrar");
                        }
                    else
                        {
                            alert(r);
                        }
                }
            });
            }
        else{
            alertify.error("Complete los datos");
        }
    });
});
</script>
